static void
rgph_test_murmur32s_data(void)
{
	uint32_t h;
	size_t i, j, l;

	j = 0;
	for (i = 0; i < sizeof(msg); i++) {
		for (l = 0; l < sizeof(msg) - i; l++) {
			h = rgph_u32_murmur32s_data(&msg[i], l, msg_seed);
			CHECK(h == msg_hashes[j]);
			j++;
		}
	}
}

static void
rgph_test_murmur32s_types(void)
{
	const uint8_t  u8[24]  = { 0x8, 1, 2, 3 };
	const uint16_t u16[24] = { 0x1661, 1, 2, 3 };
	const uint32_t u32[24] = { UINT32_C(0x22233223), 1, 2, 3 };
	const uint64_t u64[24] = { UINT64_C(0x6446644664466446), 1, 2, 3 };
	const float    f32[24] = { 32e23, 1, 2, 3 };
	const double   f64[24] = { 64e46, 1, 2, 3 };
	const uint32_t seed = 123456789;

	uint32_t h;
	size_t i;

	h = rgph_u32_murmur32s_data(u8, sizeof(u8[0]), seed);
	CHECK(rgph_u32_murmur32s_u8(u8[0], seed) == h);
	CHECK(rgph_u32_murmur32s_u8a(u8, 1, seed) == h);

	h = rgph_u32_murmur32s_data(u16, sizeof(u16[0]), seed);
	CHECK(rgph_u32_murmur32s_u16(u16[0], seed) == h);
	CHECK(rgph_u32_murmur32s_u16a(u16, 1, seed) == h);

	h = rgph_u32_murmur32s_data(u32, sizeof(u32[0]), seed);
	CHECK(rgph_u32_murmur32s_u32(u32[0], seed) == h);
	CHECK(rgph_u32_murmur32s_u32a(u32, 1, seed) == h);

	h = rgph_u32_murmur32s_data(u64, sizeof(u64[0]), seed);
	CHECK(rgph_u32_murmur32s_u64(u64[0], seed) == h);
	CHECK(rgph_u32_murmur32s_u64a(u64, 1, seed) == h);

	h = rgph_u32_murmur32s_data(f32, sizeof(f32[0]), seed);
	CHECK(rgph_u32_murmur32s_f32(f32[0], seed) == h);
	CHECK(rgph_u32_murmur32s_f32a(f32, 1, seed) == h);

	h = rgph_u32_murmur32s_data(f64, sizeof(f64[0]), seed);
	CHECK(rgph_u32_murmur32s_f64(f64[0], seed) == h);
	CHECK(rgph_u32_murmur32s_f64a(f64, 1, seed) == h);

	for (i = 0; i <= 24; i++) {
		h = rgph_u32_murmur32s_data(u8, i * sizeof(u8[0]), seed);
		CHECK(rgph_u32_murmur32s_u8a(u8, i, seed) == h);
	}

	for (i = 0; i <= 24; i++) {
		h = rgph_u32_murmur32s_data(u16, i * sizeof(u16[0]), seed);
		CHECK(rgph_u32_murmur32s_u16a(u16, i, seed) == h);
	}

	for (i = 0; i <= 24; i++) {
		h = rgph_u32_murmur32s_data(u32, i * sizeof(u32[0]), seed);
		CHECK(rgph_u32_murmur32s_u32a(u32, i, seed) == h);
	}

	for (i = 0; i <= 24; i++) {
		h = rgph_u32_murmur32s_data(u64, i * sizeof(u64[0]), seed);
		CHECK(rgph_u32_murmur32s_u64a(u64, i, seed) == h);
	}

	for (i = 0; i <= 24; i++) {
		h = rgph_u32_murmur32s_data(f32, i * sizeof(f32[0]), seed);
		CHECK(rgph_u32_murmur32s_f32a(f32, i, seed) == h);
	}

	for (i = 0; i <= 24; i++) {
		h = rgph_u32_murmur32s_data(f64, i * sizeof(f64[0]), seed);
		CHECK(rgph_u32_murmur32s_f64a(f64, i, seed) == h);
	}

	/* Test data of different lengths with different alignments. */
	for (i = 0; i < 4; i++) {
		char *s;
		size_t l;

		for (l = 0; l <= sizeof(msg); l++) {

			s = malloc(i + (l + i > 0 ? l : 1));
			REQUIRE(s != NULL);
			memcpy(s + i, msg, l);

			CHECK(rgph_u32_murmur32s_data(msg, l, seed) ==
			      rgph_u32_murmur32s_data(s+i, l, seed));

			free(s);
		}
	}
}

void
rgph_test_murmur32s(void)
{

	rgph_test_murmur32s_data();
	rgph_test_murmur32s_types();
}
