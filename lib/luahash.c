#include <stdint.h>

#include <lua.h>
#include <lauxlib.h>

#include "rgph_hash.h"

static const luaL_Reg hash_fn[] = {
	{ NULL, NULL }
};

int
luaopen_rgph_hash(lua_State *L)
{

#if LUA_VERSION_NUM <= 501
	luaL_register(L, "rgph.hash", hash_fn);
#else
	luaL_newlib(L, hash_fn);
#endif

	return 1;
}
